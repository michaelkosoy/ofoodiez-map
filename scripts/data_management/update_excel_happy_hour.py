def parse_text(text):
    lines = text.strip().split('\n')
    places = []
    current_category = "General"
    
    for line in lines:
        line = line.strip()
        if not line:
            continue
            
        # Detect categories/headers (simple heuristic: ends with ':' or is English time)
        if line.endswith(':') or re.match(r'^[A-Za-z0-9\s]+$', line):
            current_category = line.replace(':', '')
            continue
            
        # Split name and description
        # Heuristic: Name is usually the first 1-3 words before a number or %
        # This is tricky, so we'll try to split by the first occurrence of a digit
        match = re.search(r'\d', line)
        if match:
            idx = match.start()
            # Backtrack to find the last space before the number
            split_idx = line.rfind(' ', 0, idx)
            if split_idx != -1:
                name = line[:split_idx].strip()
                desc = line[split_idx:].strip()
            else:
                name = line[:idx].strip()
                desc = line[idx:].strip()
        else:
            # Fallback: take first 2 words as name
            parts = line.split(' ', 2)
            if len(parts) > 1:
                name = " ".join(parts[:2])
                desc = parts[2] if len(parts) > 2 else ""
            else:
                name = line
                desc = ""
                
        places.append({
            "Name": name,
            "Address": "",
            "Latitude": "",
            "Longitude": "",
            "Category": "Happy Hour", # Default category
            "Description": desc,
            "ImageURL": ""
        })
        
    return places

